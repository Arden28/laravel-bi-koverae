<template>
    <div class="flex relative" v-click-outside="close">
        <bi-filter-anchor :active="active" @activated="active = true">
            <span v-if="confirmedValue == null">{{ filterConfig.name }}</span>
            <span v-else>
                {{ filterConfig.name }}:
                <strong>{{ startDate }} - {{ endDate }}</strong>
            </span>
        </bi-filter-anchor>

        <bi-filter-dropdown @close="close" @reset="reset" @apply="apply" v-if="active">
            <v-date-picker
                is-expanded
                :show-day-popover="false"
                mode="range"
                v-model="datePickerValue"
                is-inline
            ></v-date-picker>
        </bi-filter-dropdown>
    </div>
</template>

<script>
import moment from "moment";

import Filter from "./Filter.vue";

export default {
    name: "bi-filter-date",
    extends: Filter,
    props: {
        value: Object
    },
    data: function() {
        return {
            datePickerValue: null
        };
    },
    watch: {
        datePickerValue: function() {
            // console.log("revaluate internal value", this.datePickerValue);
            if(this.datePickerValue) {
                this.setInternalValu({
                    start: moment(new Date(this.datePickerValue.start)).format(
                        "YYYY-MM-DD"
                    ),
                    end: moment(new Date(this.datePickerValue.end)).format(
                        "YYYY-MM-DD"
                    )
                });
            } else {
                this.setInternalValue(null);
            }
        }
    },
    computed: {
        startDate() {
            if (this.confirmedValue == null) {
                return null;
            }
            return this.confirmedValue.start;
        },
        endDate() {
            if (this.confirmedValue == null) {
                return null;
            }
            return this.confirmedValue.end;
        }
    },
    mounted() {
        this.$nextTick(() => {
            this.setDatePickerValue(this.initialOrDefaultValue);
        });
    },
    methods: {  
        setDatePickerValue(value) {
            console.log('setDatePickerValue');
            this.datePickerValue = JSON.parse(JSON.stringify(value));
        },
        close() {
            console.log('close');
            this.setInternalValue(this.confirmedOrDefaultValue);
            this.setDatePickerValue(this.confirmedOrDefaultValue);
            this.active = false;
        },
        reset: function() {
            //console.log('reset');
            this.setInternalValue(this.initialOrDefaultValue);
            this.setConfirmedValue(this.initialOrDefaultValue);
            this.setDatePickerValue(this.initialOrDefaultValue);
            this.active = false;
            this.emitValue();
        },
    }
};
</script>